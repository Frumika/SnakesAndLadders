void converIntToChar(char* text, int value)
{
    if (value == 1)
    {
        text[0] = '1';
        text[1] = '\0';
    }

    else if (value == 2)
    {
        text[0] = '2';
        text[1] = '\0';
    }

    else if (value == 3)
    {
        text[0] = '3';
        text[1] = '\0';
    }

    else if (value == 4)
    {
        text[0] = '4';
        text[1] = '\0';
    }

    else if (value == 5)
    {
        text[0] = '5';
        text[1] = '\0';
    }
    else if (value == 6)
    {
        text[0] = '6';
        text[1] = '\0';
    }
}